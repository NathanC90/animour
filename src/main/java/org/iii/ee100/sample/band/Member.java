package org.iii.ee100.sample.band;

public class Member {
	private Long id;
	private String name;
	private Long bandid;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getbandid() {
		return bandid;
	}

	public void setbandid(Long bandid) {
		this.bandid = bandid;
	}

}
